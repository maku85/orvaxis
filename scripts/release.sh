#!/usr/bin/env bash
set -euo pipefail

# Usage:
#   pnpm release           в†’ patch release  (e.g. 0.2.0 в†’ 0.2.1)
#   pnpm release:minor     в†’ minor release  (e.g. 0.2.0 в†’ 0.3.0)
#   pnpm release:major     в†’ major release  (e.g. 0.2.0 в†’ 1.0.0)
#   pnpm release:alpha     в†’ alpha prerelease (e.g. 0.2.0 в†’ 0.2.1-alpha.0)

BUMP=${1:-patch}
DIST_TAG=${2:-latest}

# в”Ђв”Ђ guard: clean working tree в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if [ -n "$(git status --porcelain)" ]; then
  echo "error: working tree is not clean вЂ” commit or stash changes first" >&2
  exit 1
fi

# в”Ђв”Ђ checks + tests в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
pnpm run check
pnpm test

# в”Ђв”Ђ bump version (no git operations yet) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if [ "$DIST_TAG" != "latest" ]; then
  NEW_VERSION=$(npm version "$BUMP" --no-git-tag-version --preid="$DIST_TAG")
else
  NEW_VERSION=$(npm version "$BUMP" --no-git-tag-version)
fi
VERSION=${NEW_VERSION#v}

echo "releasing v$VERSION (tag: $DIST_TAG)"

# в”Ђв”Ђ stamp changelog (stable releases only) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if [ "$DIST_TAG" = "latest" ]; then
  TODAY=$(date +%Y-%m-%d)
  sed -i "s/^## \[Unreleased\]$/## [$VERSION] - $TODAY/" CHANGELOG.md
  git add package.json CHANGELOG.md
else
  git add package.json
fi

# в”Ђв”Ђ commit + tag + push в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
git commit -m "chore: release v$VERSION"
git tag -a "v$VERSION" -m "v$VERSION"
git push
git push --tags

# в”Ђв”Ђ publish (prepublishOnly: check + tsc + test + build) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
npm publish --tag "$DIST_TAG"
